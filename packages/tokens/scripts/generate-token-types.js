#!/usr/bin/env node
/**
 * Generates TypeScript types for the tokens package.
 *
 * Inputs:
 *   dist/index.js    — JS constants emitted by Style Dictionary
 *   dist/tokens.css  — authoritative CSS custom properties (all modes)
 *
 * Outputs:
 *   dist/index.d.ts  — declares every JS constant with its string-literal type
 *   dist/css-vars.js — exports `cssVars` (const object) + `cssVarNames` (array)
 *   dist/css-vars.d.ts — declares `cssVars`, `cssVarNames`, and `CssVarName` union
 *
 * Consumers get autocomplete for token values and CSS var names:
 *
 *   import { cssVars, type CssVarName } from "@wyliedog/tokens/css-vars";
 *   const v: CssVarName = "--color-interactive-primary"; // ✓ autocompletes
 */

import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const distDir = join(__dirname, "..", "dist");

// ── index.d.ts ─────────────────────────────────────────────────────────────
const indexJs = readFileSync(join(distDir, "index.js"), "utf8");

// Match `export const NAME = "VALUE";` — values are always quoted strings
// (OKLCH colors, dimensions, numbers-as-strings, etc.)
const exportRegex = /^export const (\w+)\s*=\s*("(?:[^"\\]|\\.)*"|\d+(?:\.\d+)?|'[^']*');?$/gm;

const declarations = [];
let match;
while ((match = exportRegex.exec(indexJs)) !== null) {
  const [, name, rawValue] = match;
  // Preserve literal type when the value is a string; widen numerics to `number`.
  const typeAnnotation = rawValue.startsWith('"') || rawValue.startsWith("'") ? rawValue : "number";
  declarations.push(`export declare const ${name}: ${typeAnnotation};`);
}

if (declarations.length === 0) {
  console.error("⚠️  generate-token-types: no exports matched in dist/index.js — check the export pattern");
  process.exit(1);
}

const indexDts = [
  "/**",
  " * Do not edit directly, this file was auto-generated by scripts/generate-token-types.js",
  " */",
  "",
  ...declarations,
  "",
].join("\n");

writeFileSync(join(distDir, "index.d.ts"), indexDts);

// ── css-vars.{js,d.ts} ─────────────────────────────────────────────────────
const tokensCss = readFileSync(join(distDir, "tokens.css"), "utf8");

// Collect every `--name: value;` declaration. Dedupe names (light/dark/etc. repeat).
const varRegex = /--([a-zA-Z0-9-]+)\s*:\s*([^;]+);/g;
const seen = new Set();
const vars = [];
let m;
while ((m = varRegex.exec(tokensCss)) !== null) {
  const name = `--${m[1]}`;
  if (seen.has(name)) continue;
  seen.add(name);
  vars.push(name);
}
vars.sort();

// camelCase key (drop leading `--`, collapse runs of dashes, convert dashes).
// Collapsing `--` in the middle prevents producing invalid identifiers like
// `spaceShadowOffset-Lg` from `--space-shadow-offset--lg`.
const toKey = (cssVar) =>
  cssVar
    .replace(/^--/, "")
    .replace(/-+/g, "-")
    .replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());

// Build key→var map, detecting collisions from the dash-collapse step.
const keyToVar = new Map();
const collisions = [];
for (const v of vars) {
  const k = toKey(v);
  if (keyToVar.has(k) && keyToVar.get(k) !== v) {
    collisions.push({ key: k, existing: keyToVar.get(k), duplicate: v });
  } else {
    keyToVar.set(k, v);
  }
}
if (collisions.length > 0) {
  console.warn(
    `⚠️  ${collisions.length} CSS var name collision(s) after key normalization (first hidden):`
  );
  for (const c of collisions.slice(0, 5)) {
    console.warn(`   "${c.key}" -> kept "${c.existing}", dropped "${c.duplicate}"`);
  }
}

const cssVarsJs = [
  "/**",
  " * Do not edit directly, this file was auto-generated by scripts/generate-token-types.js",
  " *",
  " * Maps camelCase keys to CSS custom property names. Useful for building",
  " * inline styles or `className` strings with autocomplete.",
  " */",
  "",
  "export const cssVars = Object.freeze({",
  ...[...keyToVar.entries()].map(
    ([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`
  ),
  "});",
  "",
  "export const cssVarNames = Object.freeze([",
  ...vars.map((v) => `  ${JSON.stringify(v)},`),
  "]);",
  "",
].join("\n");

writeFileSync(join(distDir, "css-vars.js"), cssVarsJs);

const cssVarsDts = [
  "/**",
  " * Do not edit directly, this file was auto-generated by scripts/generate-token-types.js",
  " */",
  "",
  "export type CssVarName =",
  ...vars.map((v, i) => `  | ${JSON.stringify(v)}${i === vars.length - 1 ? ";" : ""}`),
  "",
  "export type CssVarKey = keyof typeof cssVars;",
  "",
  "export declare const cssVars: Readonly<{",
  ...[...keyToVar.entries()].map(
    ([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)};`
  ),
  "}>;",
  "",
  "export declare const cssVarNames: ReadonlyArray<CssVarName>;",
  "",
].join("\n");

writeFileSync(join(distDir, "css-vars.d.ts"), cssVarsDts);

console.log(
  `✓ Generated token types: ${declarations.length} constants, ${vars.length} CSS vars`
);
